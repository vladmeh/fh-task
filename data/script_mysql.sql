# DROP TABLE IF EXISTS USERS;
# DROP TABLE IF EXISTS USE_CASE;

CREATE TABLE IF NOT EXISTS USERS (
  ID BIGINT NOT NULL,
  FULL_NAME VARCHAR(100) NOT NULL,
  TELEPHONE VARCHAR(45) NOT NULL,
  SEX VARCHAR(45) NOT NULL,
  BIRTHDAY DATE NOT NULL
) ENGINE=InnoDB;
ALTER TABLE USERS ADD CONSTRAINT USERS_ID PRIMARY KEY (ID);

CREATE TABLE IF NOT EXISTS USE_CASE (
  ID BIGINT NOT NULL,
  NAME VARCHAR(100) NOT NULL,
  USE_CASE_TIME TIMESTAMP NOT NULL,
  USER_ID BIGINT NOT NULL
) ENGINE=InnoDB;
ALTER TABLE USE_CASE ADD CONSTRAINT USE_CASE_ID PRIMARY KEY (ID);
ALTER TABLE USE_CASE ADD CONSTRAINT FK_USE_CASE_USER_IDX FOREIGN KEY(USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE;
